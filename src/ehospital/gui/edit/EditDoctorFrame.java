    private void showOldDoctorData() {
         try {
            Doctor doctor = doctorService.getDoctorbyId(selectedRow);
            txtName.setText(doctor.getName());
            txtSurName.setText(doctor.getSurName());
            dtDOB.setDate(doctor.getDob());
            txtGenderID.setText(String.valueOf(doctor.getGenderID()));
            txtAddress.setText(doctor.getAddress());
            txtEmail.setText(doctor.getEmail());
            txtSalary.setText(String.valueOf(doctor.getSalary()));
            txtPhone.setText(doctor.getPhone());
            txtWorkExp.setText(String.valueOf(doctor.getWorkExperience()));
            txtPortion.setText(String.valueOf(doctor.getPortion()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
