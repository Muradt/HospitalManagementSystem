    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Doctor d = new Doctor();
            d.setName(txtName.getText());
            d.setSurName(txtSurName.getText());
            d.setDob(dtDOB.getDate());
            d.setAddress(txtAddress.getText());
            d.setEmail(txtEmail.getText());
            d.setSalary(Float.parseFloat(txtSalary.getText()));
            d.setPhone(txtPhone.getText());
            d.setWorkExperience(Integer.parseInt(txtWorkExp.getText()));
            d.setPortion(Integer.parseInt(txtPortion.getText()));
            doctorService.updateDoctor(d);
            btnResetActionPerformed(evt);
            JOptionPane.showMessageDialog(null, "Doctor Successfully updated !!!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Something went wrong !!!", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

