    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Registration registration = new Registration();
            registration.setId(selectedRow);
            registration.setPatient((Patient) cmbPatient.getSelectedItem());
            registration.setDoctor((Doctor) cmbDoctor.getSelectedItem());
            registration.setDisease((Disease) cmbDisease.getSelectedItem());
            registration.setRoom((Room) cmbRoom.getSelectedItem());
            registrationService.updateRegistration(registration);
            JOptionPane.showMessageDialog(null, "Registration Successfully updated !!!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

