    private void fillPatientCmb() {
        try {
            List<Patient> patients = patientService.getPatientList();
            DefaultComboBoxModel patientCombo = (DefaultComboBoxModel) cmbPatient.getModel();
            registration = registrationService.getRegistrationById(selectedRow);
            
            for (Patient patient : patients) {
                patientCombo.addElement(patient);
            }
            Patient patient = patientService.getPatientbyId(registration.getPatient().getId());
            patientCombo.setSelectedItem(patient);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void fillDoctorCmb() {
        try {
            List<Doctor> doctors = doctorService.getDoctorList();
            registration = registrationService.getRegistrationById(selectedRow);
            DefaultComboBoxModel doctorCombo = (DefaultComboBoxModel) cmbDoctor.getModel();
            for (Doctor doctor : doctors) {
                
                doctorCombo.addElement(doctor);
            }
            Doctor doctor = doctorService.getDoctorbyId(registration.getDoctor().getId());
            doctorCombo.setSelectedItem(doctor);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void fillDiseaseCmb() {
        try {
            List<Disease> diseases = diseaseService.getDiseaseList();
            registration = registrationService.getRegistrationById(selectedRow);
            DefaultComboBoxModel diseaseCombo = (DefaultComboBoxModel) cmbDisease.getModel();
            for (Disease disease : diseases) {
                diseaseCombo.addElement(disease);
            }
            Disease disease = diseaseService.getDiseaseById(registration.getDisease().getID());
            diseaseCombo.setSelectedItem(disease);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void fillRoomCmb() {
        try {
            List<Room> rooms = roomService.getRoomList();
            registration = registrationService.getRegistrationById(selectedRow);
            DefaultComboBoxModel roomCombo = (DefaultComboBoxModel) cmbRoom.getModel();
            for (Room room : rooms) {
                roomCombo.addElement(room);
            }
            Room room = roomService.getRoomById(registration.getRoom().getId());
            roomCombo.setSelectedItem(room);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

